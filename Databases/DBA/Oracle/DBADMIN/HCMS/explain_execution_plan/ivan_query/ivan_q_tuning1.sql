, TO_CHAR(SYSDATE,'YYYY-MM-DD'),B.POSITION_NBR, TO_CHAR(B.EFFDT,'YYYY-MM-DD'),E.SETID,E.JOBCODE
, TO_CHAR(E.EFFDT,'YYYY-MM-DD'),G.UNION_CD,TO_CHAR(G.EFFDT,'YYYY-MM-DD') 
from
(
select A.EMPLID, A.ACTION,TO_CHAR(A.ACTION_DT,'YYYY-MM-DD') ACTION_DT, A.ACTION_REASON
, TO_CHAR(A.JOB_ENTRY_DT,'YYYY-MM-DD') JOB_ENTRY_DT
,A.EMPL_STATUS, A.STEP, A.HOURLY_RT, A.COMP_FREQUENCY, A.COMPRATE, A.ANNL_BENEF_BASE_RT, A.POSITION_NBR, A.EFFSEQ
,A.GL_PAY_TYPE, D.NAME_PSFORMAT, D.SEX, TO_CHAR(D.BIRTHDATE,'YYYY-MM-DD') BIRTHDATE, D.ADDRESS1, D.CITY, D.STATE, D.POSTAL
,D.ADDRESS2, TO_CHAR(I.HIRE_DT,'YYYY-MM-DD') HIRE_DT, J.ETHNIC_GRP_CD
