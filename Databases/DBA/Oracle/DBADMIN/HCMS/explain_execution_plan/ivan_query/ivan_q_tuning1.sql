, B.REPORT_DOTTED_LINE, B.PHONE, B.MB_SAFETY_SENSITIV,G.DESCRSHORT, E.EFF_STATUS, E.SAL_ADMIN_PLAN, E.GRADE
, E.MANAGER_LEVEL, E.JOB_FAMILY, E.MED_CHKUP_REQ, E.FLSA_STATUS, E.EEO4CODE,E.JOB_FUNCTION, F.MIN_RT_ANNUAL
